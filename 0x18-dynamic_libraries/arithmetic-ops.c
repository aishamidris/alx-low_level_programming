#include <stdio.h>

/**
 * add - Add two numbers
 * @a: an integer input
 * @b: an integer input
 * Return: (a + b)
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - Subtract two numbers
 * @a: an integer input
 * @b: an integer input
 * Return: (a - b)
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - Multiply two numbers
 * @a: an integer input
 * @b: an integer input
 * Return: (a * b)
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - Divide two numbers
 * @a: an integer input
 * @b: an integer input
 * Return: (a / b)
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - Remainder of division of two numbers
 * @a: an integer input
 * @b: an integer input
 * Return: (a % b)
 */

int mod(int a, int b)
{
	return (a % b);
}
